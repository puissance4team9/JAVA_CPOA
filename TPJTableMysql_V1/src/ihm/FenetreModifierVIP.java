    private void btValidActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btValidActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat format = new SimpleDateFormat("yyyy-MM-dd");
        try {
            
            if (txNomModif.getText().isEmpty()) {
                throw new Exception("champ nom vide");
            }
            vip.setNomVip(txNomModif.getText());
            if (txPrenomModif.getText().isEmpty()) {
                throw new Exception("champ prenom vide");
            }
            vip.setPrenomVip(txPrenomModif.getText());

            vip.setCivilite(String.valueOf(jcboxCiviliteModif.getSelectedItem()));
            if (txDateNaissanceModif.getDate()==null) {
                throw new Exception("champ date de naissance vide");
            }
            vip.setDateNaissance(Date.valueOf(format.format(txDateNaissanceModif.getDate())));
            if (txLieuNaissanceModif.getText().isEmpty()) {
                throw new Exception("champ lieu de naissance vide");
            }
            vip.setLieuNaissance(txLieuNaissanceModif.getText());;

            vip.setCodeRole(String.valueOf(jcboxRoleModif.getSelectedItem()));
            if (txPaysModif.getText().isEmpty()) {
                throw new Exception("champ nom pays vide");
            }
            vip.setNomPays(txPaysModif.getText());
            etatSortie = true;
            this.dispose();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Erreur", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btValidActionPerformed

