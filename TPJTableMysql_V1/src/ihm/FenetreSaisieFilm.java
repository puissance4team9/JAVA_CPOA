    private void btValiderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btValiderActionPerformed
        // TODO add your handling code here:
        try {
            if (txNumVisa.getText().isEmpty()) {
                throw new Exception("champ numero visa vide");
            }
            film.setNumVisa(Integer.parseInt(txNumVisa.getText()));
            if (txTitre.getText().isEmpty()) {
                throw new Exception("champ titre vide");
            }
            film.setTitre(txTitre.getText());
            
            film.setGenre(String.valueOf(txGenre.getSelectedItem()));
            if (txAnnee.getText().isEmpty()) {
                throw new Exception("champ Ann√©e vide");
            }
            film.setAnnee(Integer.parseInt(txAnnee.getText()));
           
            etatSortie = true;
            this.dispose();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Erreur", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btValiderActionPerformed

