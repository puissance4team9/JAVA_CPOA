    private void btParcourirPhotoVipActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btParcourirPhotoVipActionPerformed
        // TODO add your handling code here:
        JFileChooser choix = new JFileChooser();
       
        choix.setFileSelectionMode(JFileChooser.FILES_ONLY);
        int retour =choix.showOpenDialog(this);
        ch.setChemin_dest("/public_html/");
        if (retour == JFileChooser.APPROVE_OPTION) {
            choix.getSelectedFile();
            String chemin_src=choix.getSelectedFile().getAbsolutePath(); 
            ch.setChemin_src(chemin_src);

            txPhotoVip.setText(choix.getSelectedFile().getName());
            
        }
        
    }//GEN-LAST:event_btParcourirPhotoVipActionPerformed

   

  
