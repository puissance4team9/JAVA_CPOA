    private void btValiderPhotoVipActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btValiderPhotoVipActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat format = new SimpleDateFormat("yyyy-MM-dd");
        try {
            
            
//            vip.setNomVip(txNom.getText());
            if (txPhotoVip.getText().isEmpty()) {
                throw new Exception("champ lieu de naissance vide");
            }
//            laPhotoVip.setLieu(txPhotoVip.getText());
            
            if (txDatePhotoVip.getDate()==null) {
                throw new Exception("champ date de naissance vide");
            }
            laPhotoVip.setDate(Date.valueOf(format.format(txDatePhotoVip.getDate())));
            
            if (txLieuPhotoVip.getText().isEmpty()) {
                throw new Exception("champ lieu de naissance vide");
            }
            laPhotoVip.setLieu(txLieuPhotoVip.getText());
          
            etatSortie = true;
            this.dispose();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Erreur", JOptionPane.WARNING_MESSAGE);
        }
        

    }//GEN-LAST:event_btValiderPhotoVipActionPerformed

